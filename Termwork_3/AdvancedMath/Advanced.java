package AdvancedMath;

public class Advanced {
    
    public double sine(double num){
        double num1 = Math.toRadians(num);
        return(Math.sin(num1));
    }
    public double ab(double num){
        return(Math.abs(num));
    }
    public double log(double num){
        return(Math.log10(num));
    }
}
